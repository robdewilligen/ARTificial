        6. de egel
    </p>
<br>
    <p class="text-white ml-5">
        Egels zijn een belangrijk onderdeel van de kringloop. Ze eten slakken en andere insecten die vaak een plaag vormen in de tuin. Als we de tuin natuurlijker maken, herstelt de biodiversiteit.
        Hoe maak je een egelvriendelijke tuin?
        Laat bijvoorbeeld de bladeren liggen in het najaar en hou openingen in de schuttingen zodat egels zich door tuinen heen kunnen bewegen. Laat wat meer bomen en struiken groeien, wip je tegels uit de tuin en leg een heg aan. Dat zorgt voor een fijne omgeving voor de egel. Als er veel bladeren onder een heg liggen, dan kan het zomaar zijn dat de egel daar zijn winterslaap wil houden.
        Geef geen melk aan de Egels. Melk is niet goed voor ze, ook al wordt dat vaak beweerd.
