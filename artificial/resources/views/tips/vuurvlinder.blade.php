        7. de kleine vuurvlinder
    </p>
<br>
    <p class="text-white ml-5">
        De Kleine vuurvlinder (Lycaena phlaeas) is een klein vlindertje met oranje voorvleugels en bruine achtervleugels. Het is geen grote vlinder, maar dankzij het uiterlijk valt hij wel op. De oranje en rode kleuring doet denken aan vlammetjes als hij voorbij vliegt. De kleine vuurvlinder is een standvlinder, dit betekent dat hij het hele jaar door te zien is.
        De rupsen van de vlinder zien er vrij onopvallend uit. Ze zijn groen van kleur en zijn goed verstopt in de voedselplanten.
        Vlinders zijn voor de biodiversiteit heel erg belangrijk. Dankzij hun roltong kunnen zij andere planten bestuiven dan bijen en hommels. Daarnaast spelen ze een belangrijke rol als voedsel voor broedvogels en vleermuizen. Als je voldoende vlinders ziet, betekent het dat de omgeving voldoende gevarieerd is en dat er ook allerlei andere insecten voorkomen, met andere woorden: de biodiversiteit is ontzettend hoog.
