        4. de zoete kers
    </p>
<br>
    <p class="text-white ml-5">
        Iedere boom heeft zijn eigen biotoop, een specifiek milieu dat bepaalde dieren en insecten aantrekt. De zoete kers is een vruchtdragende boom, deze hebben behalve schuil- en nestplaatsen nog meer te bieden. De zoete kers (prunus avium) is de ‘moeder van alle kersenbomen’ en komt uit de rozenfamilie. De boom staat graag licht en kan wel tot 20 meter hoog worden. In april is er een korte maar krachtige bloei van twee weken. De geurende witte bloesems trekken allerlei insecten aan. Na de bestuiving volgen de rode kersen, waar vogels weer van smullen. Zo ondersteunt één vruchtdragende boom dus de biodiversiteit op meerdere vlakken. De vruchten zijn overigens ook voor mensen prima eetbaar, hoewel ze wel een andere smaak hebben dan de gemiddelde Betuwe-kers.
        Op de zoete kers vliegen veel insecten. Voornamelijk veel soorten hommels, solitaire bijen en zweefvliegen. Honingbijen zijn groot fan van deze plant, dus dit is de perfecte boom om de bijtjes te helpen.
