        8. de Steenbeekvaren
    </p>
<br>
    <p class="text-white ml-5">
        Als u goed om zich heen kijkt, kunt de steenbreekvaren aantreffen op muren en kades. Hij groeit namelijk het liefst in een stenige, vochtige omgeving. Steden zijn daarom een ideale plek voor de steenbreekvaren. Om die reden staat de plant symbool voor de relatie tussen groen (natuur) en grijs (bebouwing).
        De steenbreekvaren was vroeger vrij zeldzaam en nam zelfs in aantal af, maar blijkt bij uitstek een plant die goed gedijt in stedelijke gebieden. En niet omdat hij stenen breekt - zoals de naam suggereert - maar juist omdat hij zich graag vestigt in barsten en gaten van (oudere) muren, kademuren en sluismuren. Hij groeit vaak op vochtige plekken, op het noorden, uit de zon. De plant heeft lichtgroene ovale blaadjes. Hij is gemakkelijk te onderscheiden van andere muurvarens, door zijn donkere nerf én omdat hij groeit op heel specifieke plekken, waar weinig ander groen is. De steenbreekvaren maakt gebruik van oude, verweerde specie, van poreus materiaal, van het 'breken van de steen'. 'De sporen vliegen er via de lucht zó in en dan kan hij gaan uitlopen. Het is een fantastisch leuk plantje. Je ziet hem bijvoorbeeld op het Grote Kerkplein, op oude sluismuren, op de lagere gedeeltes van de kades. Omdat er in Rotterdam veel water is - en veel steen - kun je hem eigenlijk in iedere buurt wel aantreffen.
