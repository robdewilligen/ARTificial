    <p class="text-white ml-5">
        De Lijnbaan is "vernoemd" naar de touwslagerij (ook wel lijnbaan genoemd) die van 1667 tot 1845 iets ten westen van de huidige Lijnbaan was gevestigd.
        Deze touwslagerij was overdekt, zodat ze alle seizoenen door konden werken. Een touwslagerij/lijnbaan maakt van hennep touw met behulp van een spinner.
        Deze lijnbanen waren soms wel 300 meter lang. Na de Tweede Wereldoorlog werd er een nieuw winkelcentrum ontworpen.
        Dit kreeg ook de naam de Lijnbaan, waar veel ondernemers twijfels over hadden.
        Zij vonden het raar dat zo'n vernieuwend en modern concept/winkelcentrum vernoemd zou worden naar een in hun ogen nederig beroep.
        De Lijnbaan destijds werd de eerste winkelpromenade die alleen toegankelijk was voor voetgangers.
        Dit trok veel toeristen naar de stad en was inspiratie voor andere steden en landen om dit ook te gaan doen.
